package java1.Loops;

public class While_loop {

	public static void main(String[] args) 
	{

		/*
		 * While:-->
		 * 			=> While is a condition based loop
		 * 			=> While accept iteration only when condition return true
		 * 			=> And stop iteration when condition return false
		 * 			=> In while loop manually we need to define increment/decrement
		 */
		
		
		int i=0;
		while(i <20)
		{
			i=i+1;
			System.out.println(i);
			
		}
		
		
		
		
		

	}

}
