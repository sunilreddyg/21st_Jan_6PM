package java1.Loops;

public class Foreach_loop {

	public static void main(String[] args) 
	{
		/*
		 * Foreach:-->
		 * 			Loop iterate array next values.
		 */
		
		//Runtime array
		String tools[]={"IDE","RC","WD","GRID","Appium"};
		
		//Example:--> Print tool names using foreach loop
		for (String eachtool : tools) 
		{
			System.out.println("=> "+eachtool);
		}

		
		
		
	}

}
