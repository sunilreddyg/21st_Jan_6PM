package java1.Loops;

public class Do_while {

	public static void main(String[] args)
	{
		
		/*
		 * Do-while:-->
		 * 		do-while is similar to while loop.Only
		 * 		diff do-while accept single iteration
		 * 		even condition return false
		 */
		
		int i=100;
		do {
			System.out.println(i);
		} while (i<20);

	}

}
